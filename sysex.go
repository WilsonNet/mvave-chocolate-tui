// BuildModeChange constructs a SysEx message to change the operating mode.
// mode is the numeric value (0x00=ProgramChangeA, 0x07=CustomCC, etc.)
func BuildModeChange(mode byte) []byte {
	msg := []byte{
		0xF0, 0x00, 0x32, 0x09, 0x49,
		0x00, 0x00, 0x00, 0x02,
		0x00, 0x00, 0x00, 0x00,
		0x10,
		0x00, 0x00, 0x00,
		mode,
		0x00, 0x00, // checksum (2 bytes)
		0xF7,
	}
	cs1, cs2 := SysexChecksum(msg[1 : len(msg)-3])
	msg[len(msg)-3] = cs1
	msg[len(msg)-2] = cs2
	return msg
}

// BuildCCConfig constructs SysEx messages to set the CC value and latching behavior
// for a specific switch index (0-15, where 0-3=1A..1D, 4-7=2A..2D, etc.)
func BuildCCConfig(switchIndex int, ccValue byte, latching bool, channel byte) []byte {
	bank := switchIndex / 4

	// CC value config: subcommand 0x02 + bank*2
	ccSubcmd := byte(0x02 + bank*2)
	ccMsg := []byte{
		0xF0, 0x00, 0x32, 0x09, 0x49,
		0x00, 0x00, 0x00, 0x02,
		ccSubcmd, 0x00, 0x00, 0x00,
		0x10,
		0x00, 0x00, 0x00,
		ccValue,
		0x00, 0x00,
		0xF7,
	}
	cs1, cs2 := SysexChecksum(ccMsg[1 : len(ccMsg)-3])
	ccMsg[len(ccMsg)-3] = cs1
	ccMsg[len(ccMsg)-2] = cs2

	// Latching config: subcommand 0x03 + bank*2
	latchSubcmd := byte(0x03 + bank*2)
	latchVal := byte(0)
	if latching {
		latchVal = 1
	}
	latchMsg := []byte{
		0xF0, 0x00, 0x32, 0x09, 0x49,
		0x00, 0x00, 0x00, 0x02,
		latchSubcmd, 0x00, 0x00, 0x00,
		0x10,
		0x00, 0x00, 0x00,
		latchVal,
		0x00, 0x00,
		0xF7,
	}
	cs1L, cs2L := SysexChecksum(latchMsg[1 : len(latchMsg)-3])
	latchMsg[len(latchMsg)-3] = cs1L
	latchMsg[len(latchMsg)-2] = cs2L

	return append(ccMsg, latchMsg...)
}

// BuildReadSettings constructs the SysEx message to request current device settings.
func BuildReadSettings() []byte {
	return []byte{
		0xF0, 0x00, 0x32, 0x0D, 0x41,
		0x00, 0x00, 0x00, 0x02,
		0x00, 0x00, 0x00, 0x00,
		0x10, 0x7E,
		0x00, 0x00, 0x07, 0x00,
		0xF7,
	}
}

// BuildDiscovery constructs the SysEx discovery message.
func BuildDiscovery() []byte {
	return []byte{
		0xF0, 0x00, 0x32, 0x45,
		0x00, 0x00, 0x00, 0x40,
		0x7F,
		0xF7,
	}
}

// SysexChecksum computes the 2-byte checksum used in Chocolate SysEx messages.
// data is the body between F0 and the checksum (excluding those framing bytes).
// For 17-byte config messages (header 00 32 09 49), the formula is:
//
//	checksum = 0x28A - sum(all data) - subcommand - value
//
// where subcommand = data[8] and value = data[len(data)-1].
// For other message types, it's: checksum = sum_constant - sum(all data).
// Returns (lowByte, highByte) in little-endian order.
func SysexChecksum(data []byte) (byte, byte) {
	var sum uint16
	for _, b := range data {
		sum += uint16(b)
	}

	switch {
	case len(data) >= 4 && data[0] == 0x00 && data[1] == 0x32 &&
		data[2] == 0x09 && data[3] == 0x49:
		// Config messages: F0 00 32 09 49 ...
		sub := uint16(data[8])
		val := uint16(data[len(data)-1])
		v := 0x28A - sum - sub - val
		return byte(v & 0x7F), byte((v >> 7) & 0x7F)

	case len(data) >= 4 && data[0] == 0x00 && data[1] == 0x32 &&
		data[2] == 0x01 && data[3] == 0x08:
		// OK response: F0 00 32 01 08 ...
		v := uint16(0x13A) - sum
		return byte(v & 0x7F), byte((v >> 7) & 0x7F)

	case len(data) >= 4 && data[0] == 0x00 && data[1] == 0x32 &&
		data[2] == 0x45:
		// Discovery: F0 00 32 45 ...
		v := uint16(0x136) - sum
		return byte(v & 0x7F), byte((v >> 7) & 0x7F)

	default:
		v := uint16(0x200) - sum
		return byte(v & 0x7F), byte((v >> 7) & 0x7F)
	}
}
